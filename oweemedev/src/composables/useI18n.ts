type StringKey = keyof typeof strings['en']

export function useI18n() {
  function t(key: StringKey): string {
    const loc = locale.value
    const table = strings[loc] as Record<string, string>
    return table[key] ?? (strings['en'] as Record<string, string>)[key] ?? key
  }

  function setLocale(l: Locale) {
    locale.value = l
    localStorage.setItem('ide_locale', l)
    window.dispatchEvent(new CustomEvent('locale-changed', { detail: l }))
  }

  const currentLocale = computed(() => locale.value)

  return { t, setLocale, currentLocale, locale }
}
